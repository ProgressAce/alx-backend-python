#!/usr/bin/env python3
"""Defines a function that concatenates (joins) two strings."""


def concat(str1: str, str2: str) -> str:
    """Joins two strings together.

    Args:
        str1(str): string 1.
        str2(str): string 2.
    Returns:
        a string combination of the two strings."""

    return str1 + str2
